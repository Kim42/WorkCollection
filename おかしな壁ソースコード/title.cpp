/*******************************************************************************
関数名:	CTitle *CTitle::Create(void)
引数:		void
戻り値:	CTitle
説明:
*******************************************************************************/
CTitle *CTitle::Create( D3DXVECTOR3 pos, D3DXVECTOR3 size )
{
	CTitle *pTitle;

	pTitle = new CTitle;

	pTitle->Init( pos, size );

	pTitle->m_nTexType = TITLE;

	return pTitle;
}

/*******************************************************************************
関数名:	HRESULT CTitle::Init(void)
引数:		HINSTANCE hInstance, HWND hWnd, BOOL bWindow
戻り値:	HRESULT
説明:
*******************************************************************************/
HRESULT CTitle::Init( D3DXVECTOR3 pos, D3DXVECTOR3 size )
{
	m_Pos = pos;
	m_Size = size;

	CScene2D::Init();

	return S_OK;
}

