#include "scene2D.h"

/*-----------------------------------------------------------------------------
 マクロ定義
-----------------------------------------------------------------------------*/
#define NUM_DIGIT_C 2

/*-----------------------------------------------------------------------------
 前方宣言
-----------------------------------------------------------------------------*/
class CNumber;

/*-----------------------------------------------------------------------------
 クラス定義
-----------------------------------------------------------------------------*/
class CCollecter : public CScene2D
{
	private:

		// ナンバー[桁数]
		CNumber *m_apNumber[NUM_DIGIT_C];

		// コレクト値
		int m_nNumCollect;

	public:

		CCollecter() { SetObjType(TYPE_COLLECTER); m_nNumCollect = 0; }
		~CCollecter() {}

		// 生成処理
		static CCollecter *Create(int nTexType, D3DXVECTOR3 pos, D3DXVECTOR3 size);

		// 各処理関数
		HRESULT Init(int nTexType, D3DXVECTOR3 pos, D3DXVECTOR3 size);
		void    Uninit(void);
		void    Update(void);
		void    Draw(void);

		// アイテム取得分の加算処理
		void AddCountItem(void) { m_nNumCollect++; }

};

