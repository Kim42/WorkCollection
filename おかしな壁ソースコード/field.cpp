#include "manager.h"
#include "field.h"

/*-----------------------------------------------------------------------------
 生成関数
-----------------------------------------------------------------------------*/
CField *CField::Create(int nTexType, D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	// 変数クラス定義
	CField *pClass;

	// インスタンスの生成
	pClass = new CField;

	// 初期化
	pClass -> Init(nTexType, pos, size);

	// 返還
	return pClass;

}

/*-----------------------------------------------------------------------------
 初期化処理関数
-----------------------------------------------------------------------------*/
HRESULT CField::Init(int nTexType, D3DXVECTOR3 pos, D3DXVECTOR3 size)
{
	// 引数の代入
	m_Pos = pos;
	m_Size = size;
	m_nTexType = nTexType;

	// オブジェクト初期化
	CScene3D::Init();

	// 正常終了
	return S_OK;

}

/*-----------------------------------------------------------------------------
 終了処理関数
-----------------------------------------------------------------------------*/
void CField::Uninit(void)
{
	CScene3D::Uninit();
}

/*-----------------------------------------------------------------------------
 更新処理関数
-----------------------------------------------------------------------------*/
void CField::Update(void)
{
	CScene3D::Update();
}

/*-----------------------------------------------------------------------------
 描画処理関数
-----------------------------------------------------------------------------*/
void CField::Draw(void)
{
	CScene3D::Draw();
}

